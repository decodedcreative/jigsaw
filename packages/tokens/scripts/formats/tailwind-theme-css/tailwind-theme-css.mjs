import {
  semanticColorAliases,
  semanticColorVars,
} from "../../utils/semantic-color-vars.mjs";
import {
  formatThemeBlock,
  paletteColorInlineDeclaration,
  tailwindV4ColorInlineRef,
  tokenToThemeDeclaration,
} from "../../utils/tailwind-theme-css.mjs";

/**
 * Style Dictionary format: Tailwind CSS v4 @theme + @theme inline configuration.
 */
export const tailwindThemeCss = ({ dictionary, options }) => {
  const header = options?.showFileHeader
    ? `/**\n * Tailwind CSS v4 theme configuration\n * Generated by Style Dictionary — do not edit directly.\n */\n\n`
    : "";

  const themeDeclarations = [];
  const inlineDeclarations = [];
  const inlineNames = new Set();

  const addInline = (declaration) => {
    if (inlineNames.has(declaration.name)) return;
    inlineNames.add(declaration.name);
    inlineDeclarations.push(declaration);
  };

  for (const token of dictionary.allTokens) {
    if (token.path[0] === "color") {
      addInline(paletteColorInlineDeclaration(token.path));
      continue;
    }

    const declaration = tokenToThemeDeclaration(token.path, token.value);
    if (declaration) themeDeclarations.push(declaration);
  }

  for (const suffix of semanticColorVars) {
    addInline({
      name: `--color-${suffix}`,
      value: tailwindV4ColorInlineRef(suffix),
    });
  }

  for (const [alias, target] of Object.entries(semanticColorAliases)) {
    addInline({
      name: `--color-${alias}`,
      value: tailwindV4ColorInlineRef(target),
    });
  }

  return `${header}${formatThemeBlock("theme", themeDeclarations)}\n\n${formatThemeBlock("theme inline", inlineDeclarations)}\n`;
};
